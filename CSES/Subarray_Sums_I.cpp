#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);

    ull n, x;
    cin >> n >> x;

    vector<ull> a(n);
    for (ull& el : a) {
        cin >> el;
    }

    ull count = 0u;
    ull sum = 0u;
    ull l, r;
    l = 0u;
    r = 0u;
    while (r < n) {
        while (r < n && sum < x) {
            sum += a[r];
            r++;
        }

        while (l <= r && x < sum) {
            sum -= a[l];
            l++;
        }

        if (sum == x) {
            count++;
            sum -= a[l];
            l++;
        }
    }

    cout << count << endl;

    return EXIT_SUCCESS;
}
